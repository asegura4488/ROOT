/**************************************************************
 *  funtion abs_d(double value)                               *
 * This program returns the absolute value of s double number *
 *************************************************************/

double abs_d(double value)
{
  
  double val;
  val = value;
  if(val < 0)
    val = (-1)*val;
  else
    return(val);
  
  return(val);
}
  
